// Да се напише програма, която прочита от клавиатурата две цели числа и извежда сумата на числата между тези две числа. 
// Въведените числа от клавиатурата не е задължително да са в ред по-малко, по-голямо. 
// Вход: 2 7 
// Изход: 18 
// Обяснение: 3+4+5+6 = 18 

#include <stdio.h>

int main()
{
    int i, j;
    printf("Enter two integers: ");
    scanf("%d %d", &i, &j);
    i += 1;
    int sum = 0;

    for(i, j; i < j; i++){
        sum += i;
    }
    printf("%d", sum);
    return 0;
}

